    private void SetText(){
        String Fecha="";
        int dia = (dcsFecha.getCalendar().get(Calendar.DAY_OF_MONTH));//Dia de JDateChoose
        int mes = (dcsFecha.getCalendar().get(Calendar.MONTH)+1);//Mes de JDateChoose
        int a単o = (dcsFecha.getCalendar().get(Calendar.YEAR));//A単o de JDateChoose
        if(mes>=1 && mes <=9){
            Fecha = a単o +"-0"+ mes +"-"+ dia; // Si el mes es entre enero y septiembre se registra con dos digitos el mes
        }
        else{
            Fecha = a単o +"-"+ mes +"-"+ dia; //Mes se registra de dos digitos por defecto
        }
        txtFecha.setText(Fecha);
    }
